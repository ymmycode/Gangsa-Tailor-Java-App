    void GetLastRow()
    {
        sql = "select nopesan from vermak ORDER BY nopesan DESC LIMIT 1"; 
            try{
                stat = (com.mysql.jdbc.Statement) connection.prepareStatement(sql);
                rs = stat.executeQuery(sql);

                if(rs.next()){
                    int id = Integer.parseInt(rs.getString("nopesan"));
                    jLabel4.setText(Integer.toString(id+1));
                    rs.close();
                    stat.close();
                }else{
                    jLabel4.setText("1");
                }
            }catch(HeadlessException | SQLException e){
                    JOptionPane.showMessageDialog(null, e);
        }
    }
    
