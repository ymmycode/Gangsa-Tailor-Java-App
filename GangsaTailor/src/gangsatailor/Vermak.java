    void ProcessInput() throws SQLException {
        //full design
        //insert design == tailor
        
        
        if ("".equals(jTextField1.getText()) || "".equals(buttonGroup1.getSelection().getActionCommand()) || "".equals(jTextField2.getText()))
        {JOptionPane.showMessageDialog(null , "Isi Form dengan benar");}
        else
        {
            String nama = jTextField1.getText();
            String tanggal = jLabel7.getText();
            String jenis = buttonGroup1.getSelection().getActionCommand();
            String kuantitas = jTextField2.getText();
            String idPesanan = jLabel4.getText();
            String pembayaran = jLabel10.getText();
            sql1 = "INSERT INTO vermak(tanggal, nama, jenis, kuantitas) VALUES ('"
                +tanggal+"','"
                +nama+"','"
                +jenis+"','"
                +kuantitas+"')";
        
            sql2 = "insert into payvermak(idPesanan, pembayaran) values ('"+idPesanan+"','"+pembayaran+"')";

            stat = (com.mysql.jdbc.Statement) connection.prepareStatement(sql1);
            stat.execute(sql1);

            stat1 = (com.mysql.jdbc.Statement) connection.prepareStatement(sql2);
            stat1.execute(sql2);

            stat.close();
            stat1.close();
        }
        
        
    }
